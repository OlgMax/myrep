<?php
/*Действия с числами*/
$a = 7;
$b = 3;
$c = 7.15;
$d = 25;
$ab = $a % $b;
$ac = $a + $c;
echo 'остаток деления ' . $a . ' на ' . $b . ' - (' . $ab . ')';
echo '</br>' . 'целая часть сложения ' . $a . ' на ' . $c . ' - (' . floor ($ac) . ')';
