echo '</br>' . $num1 . '-e' . ' слово в строке - ' . $words[$i];
$num2 = 17;
$i = $num2 - 1;
echo '</br>' . $num2 . '-й символ из фразы - ' . mb_substr($fr, $i, 1, 'UTF-8');
echo '</br>' . mb_convert_case($fr, MB_CASE_TITLE, "UTF-8");
echo '</br>' . 'длина строки ' . ' - (' . mb_strlen($fr) . ')';
echo '</br>';


